let current_timestamp_as_string () =
  let time_from_epoch = Unix.time () in
  let tm = Unix.localtime time_from_epoch in
  Printf.sprintf
    "%04i-%02i-%02i %02i:%02i:%02i"
    (tm.Unix.tm_year + 1900)
    tm.Unix.tm_mon
    tm.Unix.tm_mday
    tm.Unix.tm_hour
    tm.Unix.tm_min
    tm.Unix.tm_sec;;
